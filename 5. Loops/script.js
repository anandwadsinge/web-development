// Loops

// for loop ( I  ; C  ; U  )
// initilization; condition; update

// For loop is used when the number of iterations is already known
for(let index = 0; index < 5; index++) {
    console.log('Anand', index);
}



// While loop is used when the number of iterations is not known and we are waiting for the condition to be true.

//while 
let step = 0;                      //initilization
while (step < 5) {                 //condition
    console.log('Step', step);
    step++;                        //update
}


// The do-while loop is executed atleast once 

//do-while
let loop = 6;
do {
    console.log('loop', loop);
    loop++;
} while (loop < 5)



