//Operators

// 1. Arithmetic Operators
// Reference - https://www.w3schools.com/js/js_arithmetic.asp
// + | - | * | / | ** | %

let a = 20;
let x = (100 + 50) * a;
console.log(x);

// 2. Assignment Operator
// Reference - https://www.w3schools.com/js/js_assignment.asp

let number = 12;
number += 4;
console.log(number);  //16

// 3. Comparison & Logical Operator
// Reference - https://www.w3schools.com/js/js_comparisons.asp
// Comparison and Logical operators are used to test for true or false.

let first = 10;
let second = 5;
let result = first >= second //true
// let result = first == second //false
console.log(result);


